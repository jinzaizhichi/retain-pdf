import fs from "fs";
import path from "path";
import { execFileSync } from "child_process";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const frontendRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(frontendRoot, "..");
const desktopPackagePath = path.resolve(frontendRoot, "../desktop/package.json");
const outputPath = path.join(frontendRoot, "src/js/generated/app-version.js");

function repoFromHomepage(homepage = "") {
  const match = `${homepage || ""}`.match(/github\.com\/([^/]+\/[^/#?]+)/i);
  return match ? match[1].replace(/\.git$/i, "") : "wxyhgk/retain-pdf";
}

function normalizeGitVersion(value = "") {
  const text = `${value || ""}`.trim();
  if (!text) {
    return "";
  }
  const withoutDirty = text.replace(/-dirty$/i, "");
  const match = withoutDirty.match(/^v?\d+\.\d+\.\d+(?:-[0-9A-Za-z.-]+)?/);
  return match ? match[0].replace(/^v/i, "") : "";
}

function readGitVersion() {
  try {
    const described = execFileSync("git", ["describe", "--tags", "--always", "--dirty"], {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    });
    return normalizeGitVersion(described);
  } catch (_err) {
    return "";
  }
}

const desktopPackage = JSON.parse(fs.readFileSync(desktopPackagePath, "utf8"));
const appVersion = readGitVersion() || desktopPackage.version || "0.0.0";
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(
  outputPath,
  [
    "// Generated by scripts/generate-app-version.mjs. Do not edit by hand.",
    `export const APP_VERSION = ${JSON.stringify(appVersion)};`,
    `export const GITHUB_REPO = ${JSON.stringify(repoFromHomepage(desktopPackage.homepage))};`,
    "",
  ].join("\n"),
);

console.log(`frontend app version generated: ${appVersion}`);
